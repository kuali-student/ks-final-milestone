package org.kuali.student.lum.lu.assembly.data.client.refactorme.base;


import java.util.Date;
import org.kuali.student.common.assembly.client.Data;
import org.kuali.student.common.assembly.client.Metadata;
import org.kuali.student.lum.lu.assembly.data.client.refactorme.ConstraintMetadataBank;
import org.kuali.student.lum.lu.assembly.data.client.refactorme.base.MetaInfoHelper.Properties;


public class MetaInfoMetadata
{
	
	public boolean matches (String inputType, String inputState, String dictType, String dictState)
	{
		// TODO: code more complex matches
		return true;
	}
	
	public Metadata getMetadata (String type, String state)
	{
		Metadata mainMeta = new Metadata ();
		mainMeta.setDataType (Data.DataType.DATA);
		mainMeta.setWriteAccess (Metadata.WriteAccess.ALWAYS);
		loadChildMetadata (mainMeta, type, state);
		return mainMeta;
	}
	
	public void loadChildMetadata (Metadata mainMeta, String type, String state)
	{
		Metadata childMeta;
		Metadata listMeta;
		
		// metadata for versionInd
		childMeta = new Metadata ();
		mainMeta.getProperties ().put (Properties.VERSION_IND.getKey (), childMeta);
		childMeta.setDataType (Data.DataType.STRING);
		childMeta.setWriteAccess (Metadata.WriteAccess.NEVER);
		if (this.matches (type, state, "(default)", "(default)"))
		{
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("required"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("single"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("calc.meta.versionind"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("read.only"));
		}
		
		// metadata for createTime
		childMeta = new Metadata ();
		mainMeta.getProperties ().put (Properties.CREATE_TIME.getKey (), childMeta);
		childMeta.setDataType (Data.DataType.DATE);
		childMeta.setWriteAccess (Metadata.WriteAccess.NEVER);
		if (this.matches (type, state, "(default)", "(default)"))
		{
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("required"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("single"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("date.time"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("default.now"));
		}
		
		// metadata for createId
		childMeta = new Metadata ();
		mainMeta.getProperties ().put (Properties.CREATE_ID.getKey (), childMeta);
		childMeta.setDataType (Data.DataType.STRING);
		childMeta.setWriteAccess (Metadata.WriteAccess.NEVER);
		if (this.matches (type, state, "(default)", "(default)"))
		{
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("read.only"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("single"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("related.principalid"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("default.authenicated.principalid"));
		}
		
		// metadata for updateTime
		childMeta = new Metadata ();
		mainMeta.getProperties ().put (Properties.UPDATE_TIME.getKey (), childMeta);
		childMeta.setDataType (Data.DataType.DATE);
		childMeta.setWriteAccess (Metadata.WriteAccess.NEVER);
		if (this.matches (type, state, "(default)", "(default)"))
		{
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("required"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("single"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("date.time"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("calc.now"));
		}
		
		// metadata for updateId
		childMeta = new Metadata ();
		mainMeta.getProperties ().put (Properties.UPDATE_ID.getKey (), childMeta);
		childMeta.setDataType (Data.DataType.STRING);
		childMeta.setWriteAccess (Metadata.WriteAccess.NEVER);
		if (this.matches (type, state, "(default)", "(default)"))
		{
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("read.only"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("single"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("related.principalid"));
			childMeta.getConstraints ().add (ConstraintMetadataBank.BANK.get ("calc.authenicated.principalid"));
		}
		
	}
}

