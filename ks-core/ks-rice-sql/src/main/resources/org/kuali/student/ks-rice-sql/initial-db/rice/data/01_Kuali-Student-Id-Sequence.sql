DECLARE temp NUMBER;
BEGIN
        SELECT COUNT(*) INTO temp FROM user_sequences WHERE sequence_name = 'KS_RICE_ID_S';
        IF temp > 0 THEN EXECUTE IMMEDIATE 'DROP SEQUENCE KS_RICE_ID_S'; END IF;
END;
/

CREATE SEQUENCE KS_RICE_ID_S INCREMENT BY 1 START WITH 1000 NOMAXVALUE NOCYCLE NOCACHE ORDER
