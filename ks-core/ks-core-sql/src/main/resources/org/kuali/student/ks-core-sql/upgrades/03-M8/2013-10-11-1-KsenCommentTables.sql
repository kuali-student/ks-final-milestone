
DECLARE TEMP NUMBER;
BEGIN
  SELECT COUNT(*) INTO TEMP FROM USER_TABLES WHERE TABLE_NAME = 'KSEN_COMMENT';
	IF TEMP > 0 THEN EXECUTE IMMEDIATE 'DROP TABLE KSEN_COMMENT CASCADE CONSTRAINTS PURGE'; END IF;
END;
/




CREATE TABLE KSEN_COMMENT
(
	ID                   VARCHAR2(255) NOT NULL ,
	OBJ_ID               VARCHAR2(36) NULL ,
	COMMENT_TYPE         VARCHAR2(255) NOT NULL ,
	COMMENT_STATE        VARCHAR2(255) NOT NULL ,
	COMMENT_PLAIN        VARCHAR2(4000) NULL ,
	COMMENT_FORMATTED    VARCHAR2(4000) NULL ,
	COMMENTER_ID         VARCHAR2(255) NULL ,
	REF_OBJECT_TYPE      VARCHAR2(255) NOT NULL ,
	REF_OBJECT_ID        VARCHAR2(255) NOT NULL ,
	EFF_DT               TIMESTAMP(6) NULL ,
	EXPIR_DT             TIMESTAMP(6) NULL ,
	VER_NBR              NUMBER(19) NOT NULL ,
	CREATETIME           TIMESTAMP(6) NOT NULL ,
	CREATEID             VARCHAR2(255) NOT NULL ,
	UPDATETIME           TIMESTAMP(6) NULL ,
	UPDATEID             VARCHAR2(255) NULL
)
/

ALTER TABLE KSEN_COMMENT
	ADD CONSTRAINT  KSEN_COMENT_P PRIMARY KEY (ID)
/



